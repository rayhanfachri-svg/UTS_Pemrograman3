    private void addcartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addcartActionPerformed
        
    if (menu.getSelectedValue() == null) {
        JOptionPane.showMessageDialog(this, "Silahkan pilih menu yang mau dimasukkan ke keranjang!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return; 
    }

    
    int qty = Integer.parseInt(quantity.getValue().toString()); 
    if (qty <= 0) {
        JOptionPane.showMessageDialog(this, "Quantity minimal 1 ya!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    
    String listMenu = menu.getSelectedValue(); 
    int hargaSatuan = 0;
    String namaMenu = "";

    try {
        String[] parts = listMenu.split("-"); 
        namaMenu = parts[0].trim(); 
        hargaSatuan = Integer.parseInt(parts[1].trim()); 
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Format menu salah!");
        return;
    }

    
    int subtotal = hargaSatuan * qty; 

    
    String takeAway = "";
    if (bungkus.isSelected()) { takeAway = " (Bungkus)"; }
    else if (makansini.isSelected()) { takeAway = " (Makan Sini)"; }

    
    String itemPesanan = "- " + namaMenu + takeAway + "\t x" + qty + " \t = Rp " + subtotal + "\n";

    
    cartArea.setText(itemPesanan); 

    
    quantity.setValue(0);
        
    }//GEN-LAST:event_addcartActionPerformed

