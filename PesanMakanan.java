    private void prosesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prosesActionPerformed
        
    String noMeja = meja.getText();
    String namaPemesan = pesan.getText();
    String req = request.getText();
    
    
    String listMenu = menu.getSelectedValue(); 
    String[] parts = listMenu.split("-"); 
    int hargaSatuan = Integer.parseInt(parts[1].trim()); 
    
    
    int qty = Integer.parseInt(quantity.getValue().toString()); 
    int totalHarga = hargaSatuan * qty; 
    if (qty <= 0) {
        JOptionPane.showMessageDialog(this, "Quantity minimal 1 ya!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    
    String takeAway = "";
    if (bungkus.isSelected()) { takeAway = "Bungkus"; }
    else if (makansini.isSelected()) { takeAway = "Makan Sini"; }
    else { takeAway = "Belum Dipilih"; }

    String bayar = "";
    if (cash.isSelected()) { bayar = "Cash"; }
    else if (qris.isSelected()) { bayar = "Qris"; }
    else { bayar = "Utang"; }
     


String inputVoucher = pass.getText().trim(); 
int diskon = 0;
String infoVoucher = "-";

if (!inputVoucher.isEmpty()) { 
    if (inputVoucher.equals("232323")) {
        diskon = (totalHarga * 10) / 100; 
        totalHarga = totalHarga - diskon; 
        infoVoucher = "Sukses! Diskon 10%";
        JOptionPane.showMessageDialog(this, "Kode Voucher '" + inputVoucher + "' Berhasil!", "Voucher Berhasil Dipakai", JOptionPane.INFORMATION_MESSAGE);
    } else {
        infoVoucher = "INVALID";
        
        JOptionPane.showMessageDialog(this, "Kode Voucher '" + inputVoucher + "' tidak valid!", "Voucher Error", JOptionPane.WARNING_MESSAGE);
    }
}


    
String gabung = "--------------------------------------------"+
                "\n~ No Meja: " + noMeja +
                "\n~ Name: " + namaPemesan +
                "\n~ Order Type: " + takeAway +
                "\n--------------------------------------------" +
                "\n~ Menu: " + listMenu +
                "\n~ Quantity: " + qty +
                "\n~ Request: " + (req.isEmpty() ? "-" : req) +
                "\n--------------------------------------------" +
                "\n~ Voucher: " + infoVoucher + 
                "\n~ TOTAL BAYAR: Rp " + totalHarga +
                "\n~ Payment: " + bayar+
                "\n--------------------------------------------";


JOptionPane.showMessageDialog(this,"RINCIAN PESANAN\n\n"+ gabung + "\n\nPESANAN DI PROSES!");
    }//GEN-LAST:event_prosesActionPerformed

